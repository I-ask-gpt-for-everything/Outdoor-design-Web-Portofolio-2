const portfolioProjects = [portfolioData.photorealisticProject, ...portfolioData.completedProjects];

function getProjectById(id) {
    return portfolioProjects.find((project) => project.id === id) || null;
}

function getProjectsByType(type) {
    if (type === 'all') {
        return portfolioProjects.slice();
    }

    return portfolioProjects.filter((project) => project.type === type);
}

function getProjectTimeline(project) {
    if (!project || !Array.isArray(project.images)) {
        return { before: [], process: [], after: [] };
    }

    const sortedImages = project.images.slice().sort((a, b) => {
        const keyA = a.sortKey || a.sourceName || a.path;
        const keyB = b.sortKey || b.sourceName || b.path;
        return keyA.localeCompare(keyB);
    });

    if (project.id === 'photorealistic') {
        return { before: [], process: sortedImages, after: [] };
    }

    const beforeHints = sortedImages.filter((image) => /site|apopsi/i.test(image.sourceName || ''));
    if (beforeHints.length > 0) {
        const beforeKeys = new Set(beforeHints.map((image) => image.path));
        const remaining = sortedImages.filter((image) => !beforeKeys.has(image.path));
        if (remaining.length === 0) {
            return { before: beforeHints, process: [], after: [] };
        }

        return {
            before: beforeHints,
            process: remaining.slice(0, -1),
            after: remaining.slice(-1)
        };
    }

    if (sortedImages.length >= 3) {
        return {
            before: sortedImages.slice(0, 1),
            process: sortedImages.slice(1, -1),
            after: sortedImages.slice(-1)
        };
    }

    return { before: [], process: sortedImages, after: [] };
}

